// if 예제
const score = 85;
if (score >= 90) {
  console.log("A학점");
} else if (score >= 80) {
  console.log("B학점");
} else {
  console.log("C학점 이하");
}

// switch 예제
const fruit = "apple";
switch (fruit) {
  case "apple":
    console.log("사과");
    break;
  case "banana":
    console.log("바나나");
    break;
  default:
    console.log("알 수 없는 과일");
}




